//parseInt is used to convert a string into an integer (whole number).

let strNumber = "123";

let parsedNumber = parseInt(strNumber);

console.log(parsedNumber); // Outputs: 123

//parseFloat is used to convert a string into a floating-point number (decimal number).

let strFloat = "3.14";

let parsedFloat = parseFloat(strFloat);

console.log(parsedFloat); // Outputs: 3.14
