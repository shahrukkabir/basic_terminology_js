
// when + is used with numbers , it is used to add the numbers.

const a = 4 + 9 ;
console.log(a); // 13

// when + is used with numbers and strings , it is used to concatenate them

const b = '4' + 9 ;
console.log(b); // '49'

//not concatenate in - / * operator

const c = ' 4 ' - 2;
console.log(c);

const d = ' 4 ' / 2;
console.log(d);

const e = ' 4 ' * 2;
console.log(e);